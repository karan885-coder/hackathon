const boxes=document.querySelector('.boxes');

function getblogs(arr) {
    

arr.forEach((obj)=>{

        const cards=document.createElement('div');
        cards.classList.add('cards');

        //image
        const image=document.createElement('img');
        image.src=`images/${obj.images}.jpg`;

        //cardcontent
        const content=document.createElement('div');
        content.classList.add('content')

            const time=document.createElement('div');
            let date=Math.floor((Math.random()*31+1));
            time.innerHTML=`${date} Jan 2025 | ${obj.ago_read} min read`;

            const heading=document.createElement('div');
            heading.classList.add('heading');
            heading.innerHTML=obj.tittle;

            const para=document.createElement('div');
            para.classList.add('para');
            para.innerHTML=obj.para;
            para.addEventListener('click', () => {
                const cardData = {
                    images: obj.images,
                    tittle: obj.tittle,
                    para: obj.para,
                    date: `${date} Jan 2025`,
                    ago_read: obj.ago_read,
                };
                localStorage.setItem('cardData', JSON.stringify(cardData));
                window.location.href = 'details.html'; // Redirect to the details page
            });






            /* */
            const footer=document.createElement('div');
            footer.classList.add('footer');
                const info=document.createElement('div'); 
                info.classList.add('info');
                    const infoimg=document.createElement('img'); 
                    infoimg.src="images/admin.png";
                    const span=document.createElement('span'); 
                    span.classList.add('adminname');
                    span.innerHTML="Admin";
                    info.appendChild(infoimg);
                    info.appendChild(span);
                const heart=document.createElement('div'); 
                 heart.innerHTML = `<span class="material-symbols-outlined">favorite</span>`;
                 heart.addEventListener('click', () => {
                    heart.classList.toggle('red-bg'); // Toggle the red background
                });




            footer.appendChild(info);
            footer.appendChild(heart);

        content.appendChild(time);
        content.appendChild(heading);
        content.appendChild(para);
        content.appendChild(footer);

        cards.appendChild(image);
        cards.appendChild(content);
        
        boxes.appendChild(cards);

    })
}

getblogs(arr);

document.querySelector('.food').addEventListener('click',()=>{
      
      boxes.replaceChildren();
      getblogs(foodarr);

})
document.querySelector('.tech').addEventListener('click',()=>{
 
    boxes.replaceChildren();
    getblogs(techarr);

})
document.querySelector('.news').addEventListener('click',()=>{
 
    boxes.replaceChildren();
    getblogs(newsarr);

})


